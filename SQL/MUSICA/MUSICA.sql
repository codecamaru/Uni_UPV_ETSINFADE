CREATE TABLE ARTISTA (DNI VARCHAR(10) PRIMARY KEY, NOMBRE VARCHAR(30) NOT NULL);
CREATE TABLE GRUPO (COD CHAR(3) PRIMARY KEY, NOMBRE VARCHAR(30) NOT NULL, FECHA DATE, PAIS VARCHAR(10));
CREATE TABLE CANCION (COD INTEGER PRIMARY KEY, TITULO VARCHAR(30) NOT NULL, DURACION INTEGER);
CREATE TABLE PERTENECE (DNI VARCHAR(10) REFERENCES ARTISTA (DNI), COD CHAR(3) REFERENCES GRUPO (COD), FUNCION VARCHAR(15), PRIMARY KEY (DNI, COD));
CREATE TABLE COMPANYIA (COD CHAR(3) PRIMARY KEY, NOMBRE VARCHAR(30) NOT NULL, DIR VARCHAR(30), FAX VARCHAR(15), TFNO VARCHAR(15));
CREATE TABLE DISCO (COD CHAR(3) PRIMARY KEY, NOMBRE VARCHAR(30), FECHA DATE, COD_COMP CHAR(3) NOT NULL REFERENCES COMPANYIA (COD), COD_GRU CHAR(3) NOT NULL REFERENCES GRUPO (COD));
CREATE TABLE CLUB (COD CHAR(3) PRIMARY KEY, NOMBRE VARCHAR(30), SEDE VARCHAR(30), NUM INTEGER, COD_GRU CHAR(3) REFERENCES GRUPO (COD));
CREATE TABLE ESTA (CAN INTEGER REFERENCES CANCION (COD), COD CHAR(3) REFERENCES DISCO (COD), PRIMARY KEY (CAN, COD));

